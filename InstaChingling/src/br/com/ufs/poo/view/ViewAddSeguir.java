    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try (Connection con = new ConnectionPool().getConnection()) {
            SeguidorDAO dao = new SeguidorDAO(con);
            Seguidor seguidor = new Seguidor();
            int linha = tblUsuarios.getSelectedRow();
            int seguindoID = Integer.parseInt(String.valueOf(tblUsuarios.getValueAt(linha, 0)));
            seguidor.setUsuario_Id(this.id);
            seguidor.setSeguindo_Id(seguindoID);
            dao.salvar(seguidor);
            this.dispose();
        } catch (SQLException ex) {
            Logger.getLogger(ViewAddSeguir.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed

