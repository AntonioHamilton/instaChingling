    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        try (Connection con = new ConnectionPool().getConnection()) {
            UsuarioDAO dao = new UsuarioDAO(con);
            limparTabela();
            if (!"".equals(txtBuscaUsuario.getText())) {
                Usuario usuario = new Usuario(txtBuscaUsuario.getText(), "", "", "");
                dao.buscaUsuarioNome(usuario);
                if(usuario.getId() != null){
                    DefaultTableModel model = (DefaultTableModel) tblUsuarios.getModel();
                    model.addRow(new Object[]{usuario.getId(), usuario.getNome()});
                }
            } else {
                List<Usuario> usuarios = dao.lista(this.id);
                usuarios.stream().forEach((usuario2) -> {
                    DefaultTableModel model = (DefaultTableModel) tblUsuarios.getModel();
                    model.addRow(new Object[]{usuario2.getId(), usuario2.getNome()});
                });
            }

        } catch (SQLException ex) {
            Logger.getLogger(ViewAddSeguir.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

